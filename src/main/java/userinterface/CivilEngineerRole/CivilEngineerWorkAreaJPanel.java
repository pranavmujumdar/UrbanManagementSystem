    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[6];
            
            row[0] = request;
            row[1] = request.getReceiver();
            row[2] = request.getStatus();
            //String result = ((LabTestWorkRequest) request).getTestResult();
            //row[3] = result == null ? "Waiting" : result;
            row[3] = request.getResult2();
            row[4] = request.getRequestDate() == null ? "NA" : request.getRequestDate();
            row[5] = request.getResolveDate() == null ? "NA" : request.getResolveDate();
            //row[3] = result == null ? "Waiting" : result;
            
            if(request.getStatus().equals("Completed")|| request.getStatus().equals("Declined"))
            {
                userAccount.getWorkQueue().getProcessingWorkList().remove(row);
            }
            
            model.addRow(row);
        }
    }
    

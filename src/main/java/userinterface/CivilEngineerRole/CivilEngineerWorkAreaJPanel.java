    private void assignJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButton1ActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }

        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
        
        if(request.getReceiver()!=null)
        {
            if(request.getReceiver()==userAccount)
            {
                JOptionPane.showMessageDialog(null, "The selected request is already assigned to you");
                return;

            }
            else
            {
                JOptionPane.showMessageDialog(null, "The selected request is assigned to someone else.");
                return;
            }
        }

        else if(userAccount.getWorkQueue().getProcessingWorkList().size()>0)
        {
            JOptionPane.showMessageDialog(null, "Please process your current assignment before taking on a new request." + userAccount.getWorkQueue().getProcessingWorkList().size());
            return;
        }
        else {
            request.setReceiver(userAccount);
            request.setStatus("Pending");
            userAccount.getWorkQueue().getProcessingWorkList().add(request);
            populateRequestTable();
        }

    }//GEN-LAST:event_assignJButton1ActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:

        int selectedRow = workRequestJTable.getSelectedRow();
        if(selectedRow >= 0){
            RoadConstructionWorkRequest request = (RoadConstructionWorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
            ViewDetailsAreaJPanel ViewDetailsAreaJPanel = new ViewDetailsAreaJPanel(userProcessContainer, request, userAccount, enterprise, system, network, organization);
            userProcessContainer.add("processWorkRequestJPanel", ViewDetailsAreaJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        }
        else{
            JOptionPane.showMessageDialog(null, "select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

